public class jordanaa_0117Lab5 {
	public static void main(String [] args) {
		System.out.println("For loop 1");
		for (int i = 10; i <= 50; i += 10) {
			System.out.println(i);
		}
		System.out.println();
		System.out.println("For loop 2");
		for (int i = 20; i >= 10; i -= 2) {
			System.out.println(i);
		}
	}
}
